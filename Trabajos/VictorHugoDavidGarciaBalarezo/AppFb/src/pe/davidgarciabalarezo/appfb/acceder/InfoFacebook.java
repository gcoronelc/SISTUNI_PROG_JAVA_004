public class InfoFacebook {
    private String usuario;
    private String urlmiimagen;   
    private String email;    
    private String genero;
    private String idfacebook;
    private String post;
    private Integer likes;

    /**
     * @return the usuario
     */
    public String getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    /**
     * @return the urlmiimagen
     */
    public String getUrlmiimagen() {
        return urlmiimagen;
    }

    /**
     * @param urlmiimagen the urlmiimagen to set
     */
    public void setUrlmiimagen(String urlmiimagen) {
        this.urlmiimagen = urlmiimagen;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the genero
     */
    public String getGenero() {
        return genero;
    }

    /**
     * @param genero the genero to set
     */
    public void setGenero(String genero) {
        this.genero = genero;
    }

    /**
     * @return the idfacebook
     */
    public String getIdfacebook() {
        return idfacebook;
    }

    /**
     * @param idfacebook the idfacebook to set
     */
    public void setIdfacebook(String idfacebook) {
        this.idfacebook = idfacebook;
    }

    /**
     * @return the post
     */
    public String getPost() {
        return post;
    }

    /**
     * @param post the post to set
     */
    public void setPost(String post) {
        this.post = post;
    }

    /**
     * @return the likes
     */
    public Integer getLikes() {
        return likes;
    }

    /**
     * @param likes the likes to set
     */
    public void setLikes(Integer likes) {
        this.likes = likes;
    }
 
   
}
